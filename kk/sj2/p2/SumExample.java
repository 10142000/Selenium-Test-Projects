package com.kk.sj2.p2;

public class SumExample {

	public static void main(String[] args) {

//		Write a program to calculate the sum of first 10 natural number.
		
	int i,num=10,sum = 0;
	for(i=1;i<=num;++i) {
		sum=sum+i;
	}
	System.out.println("sum of first 10 natural number is"+sum);
	}

}
